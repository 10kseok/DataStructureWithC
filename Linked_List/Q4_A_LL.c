void moveEvenItemsToBack(LinkedList *ll)
{
	// 짝수는 맨 뒤로 보내는 함수
	ListNode *cur_node = ll->head;
	ListNode *prev_node = NULL;
	int count = ll->size;
	while (count > 0) {
		if (cur_node->next == NULL) {
			break;
		}
		int item = cur_node->item;
		// 짝수인 경우, 이전값만 기록하고 다음 노드로 이동
		if (item % 2 == 1) {
			prev_node = cur_node;
			cur_node = cur_node->next;
			count--;
			continue;
		}
		// 홀수인 경우
		// - 현재 노드가 헤드인지 아닌지에 따라 다르게 처리
		ListNode *even_node, *next_node;
		even_node = cur_node;
		if (prev_node == NULL) {
			// 현재 노드가 헤드거나, 홀수만 있는 경우
			// - 노드가 하나면 즉시 종료.
			ll->head = cur_node->next;
			next_node = ll->head;
		} else {
			prev_node->next = even_node->next;
			next_node = even_node->next;
		}

		// - (공통) 짝수를 마지막 노드로 연결해준다.
		while (cur_node->next != NULL) {
			cur_node = cur_node->next;
		}
		cur_node->next = even_node;
		cur_node->next->next = NULL;
		count--;
		// 다음으로 순회할 노드로 변경
		cur_node = next_node;
	}
}

